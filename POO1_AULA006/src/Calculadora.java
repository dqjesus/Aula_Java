public class Calculadora 
{

	public int soma (int a, int b)
	{
		int result;
		result = (a + b);
		return result;
	}

	public int subtracao (int a, int b)
	{
		int result;
		result = (a - b);
		return result;
	}

	public int multiplicacao (int a, int b)
	{
		int result;
		result = (a * b);
		return result;
	}

	public int divisao (int a, int b)
	{
		int result;
		result = (a / b);
		return result;
	}

}